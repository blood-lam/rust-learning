/** Rust Primitive Data Types
 * 
 * Integers:
 * - Signed: i8, i16, i32, i64, i128, isize
 * - Unsigned: u8, u16, u32, u64, u128, usize
 * 
 * Floating-Point:
 * - f32: single precision
 * - f64: double precision (default)
 * 
 * Boolean:
 * - bool: true or false
 * 
 * Characters:
 * - char: Unicode scalar value (4 bytes)
 * 
 * Compound Types:
 * - Tuples: Fixed-length collection of values
 * - Arrays: Fixed-length collection of same type
 * 
 * Special Types:
 * - unit type: () empty tuple
 * - str: String slice
 */